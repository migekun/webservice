package asbaprin.ws.soap.data;

public class GenericData {

	private int id;
	private String nom;
	
	public GenericData() {}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	
	
}
